public class RelationalOperator {
    public static void main(String[] args) {
        int a=30, b=10;
        System.out.println(a==b);//false
        System.out.println(a<b);//false
        System.out.println(a>b);//true
        System.out.println(a<=b);//false
        System.out.println(a>=b);//true
        System.out.println(a!=b);//true
        System.out.println(a==b);//false
         System.out.println(a<<b);//30720
         System.out.println(a>>b);//0
         System.out.println(a&b);//0
         System.out.println(a|b);//30
        
    }
}
