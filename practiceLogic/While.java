import java.util.Scanner;

public class While {
    public static void main(String[] args)
    {
        Scanner sc=new Scanner(System.in);
        System.out.println("Lucky number is");
        int num=sc.nextInt();

        int n=1;
        while(n<=num){
    System.out.println(n);
    n++;
        }
       }
       }
